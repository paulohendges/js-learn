// sempre utilizado para variáveis que podem variar
let nome = 'João';

// Não podemos criar variáveis com palavras reservadas;
// Variáveis precisam ter nomes significativos;
// Não devemos começar o nome de uma variável com um número;
// Não podem conter espaços ou traços;
// Sempre utilizar camelCase;
// Variáveis em JS são case-sensitive -> nomeCliente !== nomecliente
// Não se pode re-criar variáveis com LET

console.log(nome, 'nasceu em 1984.');
console.log('Em 2000', nome, 'conheceu Maria.');
console.log(nome, 'casou-se com Maria em 2012.');
console.log('Maria teve 1 filho com', nome, 'em 2015.');
console.log('O filho de', nome, 'se chama Eduardo.');